"""
Apppend:-  append() adds a single element to the end of the list while .

Extend:- extend() can add  several items to a list as individual items.

"""
# Append :

List=[1,2,3,4,5]
print(List)
List.append(6)
print(List)



#Extend :

List1=[1,2,3,4,5]
print(List1)

#create new list2 and add to List1
List2=[6,7,8,9,10]
#extend
List1.extend(List2)
print(List1)











